$(function () {
    $('#fileInput').change(function () {
        var fileName = $(this).val().split('\\').pop(); 
        $(this).siblings('.custom-file-upload').text('File Selected').css({'color': 'white', 'background-color': '#81689d'});
        $('').text(fileName);
    });
});

